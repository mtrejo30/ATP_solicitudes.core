package com.atp.solicitudes.reports.model;

import java.util.Date;

public class ConsultaBooking
{
	private String contenedor;
	private String lhtCode;
	private String sslCode;
	private Date rdDate;
	private String rdFlag;
	private String movimiento;

	public String getContenedor()
	{
		return contenedor;
	}

	public void setContenedor(String contenedor)
	{
		this.contenedor = contenedor;
	}

	public String getLhtCode()
	{
		return lhtCode;
	}

	public void setLhtCode(String lhtCode)
	{
		this.lhtCode = lhtCode;
	}

	public String getSslCode()
	{
		return sslCode;
	}

	public void setSslCode(String sslCode)
	{
		this.sslCode = sslCode;
	}

	public Date getRdDate()
	{
		return rdDate;
	}

	public void setRdDate(Date rdDate)
	{
		this.rdDate = rdDate;
	}

	public String getRdFlag()
	{
		return rdFlag;
	}

	public void setRdFlag(String rdFlag)
	{
		this.rdFlag = rdFlag;
	}

	public String getMovimiento()
	{
		return movimiento;
	}

	public void setMovimiento(String movimiento)
	{
		this.movimiento = movimiento;
	}

}
