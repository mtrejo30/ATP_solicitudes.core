package com.atp.solicitudes.reports.model;

import java.util.Date;

public class ArrivalTimeContenedor
{
	private String appNBR;
	private String contenedor;
	private Date createdDate;
	private Date inLaneDate;

	public String getAppNBR()
	{
		return appNBR;
	}

	public void setAppNBR(String appNBR)
	{
		this.appNBR = appNBR;
	}

	public String getContenedor()
	{
		return contenedor;
	}

	public void setContenedor(String contenedor)
	{
		this.contenedor = contenedor;
	}

	public Date getCreatedDate()
	{
		return createdDate;
	}

	public void setCreatedDate(Date createdDate)
	{
		this.createdDate = createdDate;
	}

	public Date getInLaneDate()
	{
		return inLaneDate;
	}

	public void setInLaneDate(Date inLaneDate)
	{
		this.inLaneDate = inLaneDate;
	}
}
